function hideComments(classId)
{
	var x = document.getElementsByClassName("classId");
	var i;
		for (i = 0; i < x.length; i++) {
			x[i].hide()
		}
}

function showComments(classId)
{
	var x = document.getElementsByClassName("classId");
	var i;
		for (i = 0; i < x.length; i++) {
			x[i].hide()
		}
}